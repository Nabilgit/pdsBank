package edu.god.bank.bean;

/**
 *
 * @author Nabil
 */
public class EstateValue {
 
    private float realestate;
    private float capitalestate;
    private float assetsestate;
    private float donation;
    private float investement;

    public float getRealestate() {
        return realestate;
    }

    public void setRealestate(float realestate) {
        this.realestate = realestate;
    }

    public float getCapitalestate() {
        return capitalestate;
    }

    public void setCapitalestate(float capitalestate) {
        this.capitalestate = capitalestate;
    }

    public float getAssetsestate() {
        return assetsestate;
    }

    public void setAssetsestate(float assetsestate) {
        this.assetsestate = assetsestate;
    }

    public float getDonation() {
        return donation;
    }

    public void setDonation(float donation) {
        this.donation = donation;
    }

    public float getInvestement() {
        return investement;
    }

    public void setInvestement(float investement) {
        this.investement = investement;
    }
          
 
    
}
